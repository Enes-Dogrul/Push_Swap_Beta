#include "libft.h"
#include <limits.h>

int	ft_atoi(const char *str)
{
	unsigned long	num;
	int				i;
	int				np;

	np = 1;
	i = 0;
	num = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\f'
		|| str[i] == '\r' || str[i] == '\n' || str[i] == '\v')
		i++;
	if (str[i] == '+' || str[i] == '-')
		if (str[i++] == '-')
			np = -1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - '0');
		i++;
	}
	if (num > LONG_MAX)
	{
		if (np == -1)
			return (0);
		return (-1);
	}
	return ((int)(np * num));
}
